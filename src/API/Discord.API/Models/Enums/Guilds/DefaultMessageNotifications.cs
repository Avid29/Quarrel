namespace Discord.API.Models.Enums.Guilds
{
    /// <summary>
    /// An enum representing the default message notifications for a guild.
    /// </summary>
    public enum DefaultMessageNotifications
    {
        /// <summary>
        /// All messages have notifications by default.
        /// </summary>
        AllMessages = 0,

        /// <summary>
        /// Only mentions have notifications by default.
        /// </summary>
        MentionsOnly = 1
    }
}
